    init {
        check(intervalStarts.size == intervalCount)
        check(intervalClassCodes.size == intervalCount)
    }

    fun classify(scalar: Int): SourcedTextScalarClass {
        if (scalar !in 0..0x10ffff || scalar in 0xd800..0xdfff) {
            throw SourcedMapTextException("script lookup requires a Unicode scalar")
        }
        var low = 0
        var high = intervalStarts.lastIndex
        while (low <= high) {
            val middle = (low + high) ushr 1
            if (intervalStarts[middle] <= scalar) {
                low = middle + 1
            } else {
                high = middle - 1
            }
        }
        return when (intervalClassCodes[high].toInt()) {
            0 -> SourcedTextScalarClass.NEUTRAL
            1 -> SourcedTextScalarClass.STRONG_LATIN
            2 -> SourcedTextScalarClass.STRONG_NON_LATIN
            3 -> SourcedTextScalarClass.UNKNOWN
            else -> throw IllegalStateException("compiled Unicode script class is invalid")
        }
    }
